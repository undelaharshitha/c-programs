#include<stdio.h>
int main(){
  int a=10;  //a is assigned with a value 10
  printf("a:%d\n",a);
  a+=4;  // a=a+6
  printf("a:%d\n",a);
  a-=3;  //a=a-3
  printf("a:%d\n",a);
  a*=2;  //a=a*2
  printf("a:%d\n",a);
  a/=11; //a=a/11
  printf("a:%d\n",a);
  a%=2;  //a=a%2
  printf("a:%d\n",a);
   return 0;
}
